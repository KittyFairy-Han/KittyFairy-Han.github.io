/*
 窗口改变时-------------------------------------------------------------------------------------------------------------
 =======================================================================================================================
 -----------------------------------------------------------------------------------------------------------------------
 */
$(window).resize(function() {
    var ww=parseInt($("body").css("width"));
    if(ww<=1000){
        $('h1').css("font-size","40px");
        $("#vr360 .projects .project").css("width","47%")
    }
    else {
        $('h1').css("font-size","50px");
        $("#vr360 .projects .project").css("width","30%")
    }
});
